import React, { useEffect, useState, useRef } from 'react';
import { useLocation, useNavigate } from 'react-router-dom';
import { Box, Text, Spinner, Card, Button } from '@nimbus-ds/components';
// Importar o axios diretamente, não o do app que tem configuração do Nexo
import axiosStandard from 'axios';

// URL da API (sem barra no final para evitar duplicação)
const API_URL = (import.meta.env.VITE_API_URL || '').replace(/\/$/, '');

const InstallPage: React.FC = () => {
    const [status, setStatus] = useState<'loading' | 'success' | 'error'>('loading');
    const [message, setMessage] = useState<string>('Processando instalação...');
    const location = useLocation();
    const navigate = useNavigate();
    const hasCalled = useRef(false);

    useEffect(() => {
        const handleInstall = async () => {
            if (hasCalled.current) return;
            hasCalled.current = true;

            try {
                // Extrair o código da query string
                const searchParams = new URLSearchParams(location.search);
                const code = searchParams.get('code');

                if (code) {
                    console.log('Iniciando instalação com código:', code);
                    setStatus('loading');
                    setMessage('Conectando ao servidor de instalação...');

                    // Fazer a chamada para o endpoint da API (através do proxy /api)
                    const response = await axiosStandard.get(`${API_URL}/api/ns/install?code=${code}`, {
                        withCredentials: true,
                        headers: {
                            'Accept': 'application/json',
                            'Content-Type': 'application/json'
                        }
                    });
                    console.log('Instalação finalizada:', response);

                    if (response.data && response.data.success) {
                        setStatus('success');
                        setMessage('Instalação concluída com sucesso! Redirecionando de volta pra loja...');

                        // Redirecionar para a página de login da Nuvemshop após a instalação
                        setTimeout(() => {
                            window.location.href = 'https://www.nuvemshop.com.br/login';
                        }, 3000);
                    } else {
                        setStatus('error');
                        setMessage(`Erro: ${response.data?.message || 'Falha na instalação'}`);
                    }
                } else {
                    console.error('Código de instalação não encontrado');
                    setStatus('error');
                    setMessage('Erro: Código de instalação não encontrado');
                }
            } catch (error: any) {
                console.error('Erro durante o processo de instalação:', error);
                setStatus('error');
                setMessage(`Erro durante instalação: ${error.message || 'Falha na conexão'}`);
            }
        };

        handleInstall();
    }, [location, navigate]);

    return (
        <Box
            height="100vh"
            display="flex"
            justifyContent="center"
            alignItems="center"
            padding="4"
            backgroundColor="primary-surface"
        >
            <Box width="100%" style={{ maxWidth: '900px' }}>
                <Card padding="large">
                    <Box marginBottom="6" borderBottomWidth="1" borderBottomStyle="solid" borderBottomColor="neutral-surface" paddingBottom="4">
                        <Text as="h2" fontSize="6" fontWeight="bold">Instalação do Aplicativo</Text>
                    </Box>
                    <Box
                        display="flex"
                        flexDirection="column"
                        alignItems="center"
                        gap="6"
                        paddingY="8"
                    >
                        {status === 'loading' && (
                            <Box display="flex" flexDirection="column" alignItems="center" gap="4">
                                <Spinner size="large" />
                                <Text color="neutral-textLow">Isso deve levar apenas alguns instantes...</Text>
                            </Box>
                        )}

                        <Text
                            textAlign="center"
                            fontSize="4"
                            fontWeight={status === 'error' ? 'bold' : 'regular'}
                            color={
                                status === 'error'
                                    ? 'danger-textHigh'
                                    : status === 'success'
                                        ? 'success-textHigh'
                                        : 'neutral-textHigh'
                            }
                        >
                            {message}
                        </Text>
                    </Box>
                    {status === 'error' && (
                        <Box display="flex" gap="2" justifyContent="center" paddingTop="4">
                            <Button
                                appearance="primary"
                                onClick={() => (window.location.href = 'https://www.nuvemshop.com.br/login')}
                            >
                                Voltar para a Nuvemshop
                            </Button>
                        </Box>
                    )}
                </Card>
            </Box>
        </Box>
    );
};

export default InstallPage;
